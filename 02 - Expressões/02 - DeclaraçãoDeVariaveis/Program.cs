int b; // Declaração de variável do tipo inteiro
float c; // Declaração de variável do tipo float

a = 5; // Armazenamento de valor 5 em a
b = 10; // Armazenamento de valor 10 em b
c = 5.3f; // Armazenamento de valor 5.3 em c

// Delacrando váriaveis de uma só vez

int d, e; // Declara duas variáveis do tipo inteiro

// Dessa forma poderiamo substituir a declação anterior int a, b; e atribuir os valores a e b.

// Não é possivel declarar variáveis de tipos diferentes na mesma linha, como por exemplo:

int f;
f = 4.3; //Variável só armazenar o valor 4 e não o 4.3, pois o tipo dela é inteiro e não float. 

// Em C# as variaveis já podem iniciar com valor, como por exemplo:

int g = 10, h = 20; // Declaração de variável do tipo inteiro e atribui valor 10 a g e 20 a h
